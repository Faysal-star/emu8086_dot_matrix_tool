.data
Dots	DB 00000010b, 00111110b, 00000010b, 00011110b, 00100010b ;1
        DB 01000001b, 01001001b, 01001001b, 01001001b, 01111111b ;2
        DB 00000000b, 00011100b, 00011100b, 00011100b, 00000000b ;3
        DB 00000000b, 01000010b, 01111111b, 01000000b, 00000000b ;4
        DB 00001100b, 00001010b, 01111111b, 00001000b, 00000000b ;5
        DB 00000000b, 01000010b, 01111111b, 01000000b, 00000000b ;6
        DB 01111111b, 00001001b, 00001001b, 00000001b, 00000001b ;7
        DB 00000000b, 00000000b, 00000000b, 00000000b, 00000000b ;8

.code
START   PROC    FAR
    PUSH    DS
    MOV     AX, 0
    PUSH    AX
	MOV     AX, @data
    MOV     DS, AX
    MOV     ES, AX


	MOV DX,2000h	; first DOT MATRIX Port address
	MOV BX, 0

MAINLOOP:
	MOV SI, 0
	MOV CX, 5

NEXT:
	MOV AL,Dots[BX][SI]
	out dx,al
	INC SI
	INC DX

	CMP SI, 5
	LOOPNE NEXT

	ADD BX, 5
	CMP BX, 40
	JL MAINLOOP
    RET
START   ENDP

END    START

